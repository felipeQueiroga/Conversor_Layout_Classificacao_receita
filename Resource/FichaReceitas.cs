using Converte_SIAxCTBLT.Models;
using System;
using System.Collections.Generic;

namespace Converte_SIAxCTBLT.Config
{
    class FichaReceitas
    {
        private string Path = @"C:\LayOut CTBL\conf.txt";
        public Dictionary<string, string> Fichas = new Dictionary<string, string>();

        public FichaReceitas()
        {
            carregaFichas();
        }

        private void carregaFichas()
        {
            List<String> linhasArquivo = StreamArquivo.lerAquivo(Path);
            foreach (var item in linhasArquivo)
            {
                string[] chaveValor = item.Split(";");
                Fichas.Add(chaveValor[0], chaveValor[1]);
            }
        }
    }
}

